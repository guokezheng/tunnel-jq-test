package com.tunnel.business.datacenter.domain.enumeration;


/**
 * 故障类型
 *
 * @author zhai
 */
public enum FaultStatusEnum {

    DEVICE_RELEASE("0", "已发布"),
    DEVICE_NO_RELEASE("1", "未发布"),
    DEVICE_REMOVE("0", "已消除"),
    DEVICE_NO_REMOVE("1", "未消除");
    private String code;
    private String name;

    /**
     * 判断是否包含
     *
     * @param code
     * @return
     */
    public static Boolean contains(String code) {
        if (null == code) {
            return false;
        }
        for (FaultStatusEnum typeEnum : FaultStatusEnum.values()) {

            if (code.equals(typeEnum.code)) {
                return true;
            }
        }
        return false;
    }

    /**
     * 传code返回name
     *
     * @param code
     * @return
     */
    public static String getValue(String code) {
        // 遍历枚举
        for (FaultStatusEnum value : FaultStatusEnum.values()) {
            String s = value.getCode() + "";
            if (s.equals(String.valueOf(code))) {
                return value.getName();
            }
        }
        // 其他情况
        return null;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    FaultStatusEnum(String code, String name) {
        this.code = code;
        this.name = name;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setName(String name) {
        this.name = name;
    }
}
